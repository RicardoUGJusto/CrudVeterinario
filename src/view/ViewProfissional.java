    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
      
            if (jTProfissional.getSelectedRow() != -1) {
                tableModel.setValueAt(txtcrmv.getText(), jTProfissional.getSelectedRow(), 0);
                tableModel.setValueAt(txtnome.getText(), jTProfissional.getSelectedRow(), 1);
                tableModel.setValueAt(txtespecialidade.getText(), jTProfissional.getSelectedRow(), 2);
                tableModel.setValueAt(txthorario.getText(), jTProfissional.getSelectedRow(), 3);
                jTProfissional.clearSelection();
                limparCampos();
            }
      
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (!txtcrmv.getText().isEmpty()) {
        
        Profissional p = new Profissional();
        p.setCrmv(txtcrmv.getText());
        p.setNome(txtnome.getText());
        p.setEspecialidade(txtespecialidade.getText());
        p.setHorario(txthorario.getText());

        tableModel.addRow(p);
        limparCampos();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void limparCampos() {
        txtcrmv.setText("");
        txtnome.setText("");
        txtespecialidade.setText("");
        txthorario.setText("");
    }

