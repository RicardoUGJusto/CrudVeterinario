    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (jTProfissional.getSelectedRow() != -1) {

            tableModel.removeRow(jTProfissional.getSelectedRow());
            limparCampos();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    
    private void jTProfissionalMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTProfissionalMouseClicked
        int linhaSelecionada = jTProfissional.getSelectedRow();

        txtcrmv.setText((String) jTProfissional.getValueAt(linhaSelecionada, 0));
        txtnome.setText((String) jTProfissional.getValueAt(linhaSelecionada, 1));
        txtespecialidade.setText((String) jTProfissional.getValueAt(linhaSelecionada, 2));
        txthorario.setText((String) jTProfissional.getValueAt(linhaSelecionada, 3));

    }//GEN-LAST:event_jTProfissionalMouseClicked

